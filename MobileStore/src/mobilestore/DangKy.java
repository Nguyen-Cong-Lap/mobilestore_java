    private void btndangkyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndangkyActionPerformed
        try{
            if(txttaikhoandk.getText().equals("")||txtmatkhaudn.getText().equals("")){
                JOptionPane.showMessageDialog(this, "Ban can nhap du cac truong !!");
            }
            else{
                StringBuffer sb=new StringBuffer();
                String sql_check ="select*from NguoiDung where taikhoan='"+txttaikhoandk.getText()+"'";
                Statement st=conn.createStatement();
                ResultSet rs=st.executeQuery(sql_check);
                if(rs.next()){
                    sb.append("ban da co tai khoan vui long dang nhap");
                }if(sb.length()>0){
                    JOptionPane.showMessageDialog(this,sb.toString());
                }else{
                    String sql="insert into NguoiDung values('"+txttaikhoandk.getText()+
                        "','"+txtmatkhaudn.getText()+"')";
                    st=conn.createStatement();
                    int kq=st.executeUpdate(sql);
                    if(kq>0){
                        JOptionPane.showMessageDialog(this,"Dang ky thanh cong");
                    }else{
                        JOptionPane.showMessageDialog(this,"that bai");
                    }
                }
            }
        }catch(Exception  ex){
            System.out.println(ex.toString());
        }
    }//GEN-LAST:event_btndangkyActionPerformed

